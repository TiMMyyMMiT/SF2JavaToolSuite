    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        Path paletteEntriesPath = PathHelpers.getBasePath().resolve(fileButton1.getFilePath());
        Path weaponSpritePath = PathHelpers.getBasePath().resolve(fileButton3.getFilePath());
        try {
            weaponspriteManager.importImage(paletteEntriesPath, weaponSpritePath);
        } catch (Exception ex) {
            weaponspriteManager.clearData();
            Console.logger().log(Level.SEVERE, null, ex);
            Console.logger().severe("ERROR Weapon sprite image could not be imported from : " + weaponSpritePath);
        }
        onDataLoaded();
    }//GEN-LAST:event_jButton12ActionPerformed

